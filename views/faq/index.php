            <p>Zajęcia w szkole Skarlet są zorganizowane w ten sposób, że w każdym tygodniu są dwa spotkania (każde trwające 2 godziny zegarowe, lub 1 godzinę i 40 minut). Pierwsze spotkanie odbywa się w poniedziałek, lub wtorek a drugie we wtorek,lub środę. Najlepiej zdecydować się na stałe na określone terminy i ich się trzymać tak, by w każdym tygodniu być najpierw na pierwszym spotkaniu a później na drugim. Na przykład jeśli chodzisz na pierwsze spotkanie w poniedziałek to na drugie możesz pójść zarówno w środę, jak i czwartek. Każdy stopień to pięć kolejnych spotkań po 2 godziny zegarowe (lub sześć spotkań po 1 godz, 40 min. ) plus do tego zajęcia nieobowiązkowe w piątki (wprawki/ćwiczenia/praktisy) czyli łącznie ponad 23 godziy lekcyjne nauki. Wprawki to zajęcia dodatkowe na których samodzielnie szkoli się swoje umiejętności.</p>
